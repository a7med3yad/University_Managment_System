    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int std_id = Integer.parseInt(id.getText());
        int section_id  = Integer.parseInt(Section_ID.getText());
        String grade = Grade.getText();
      
        try (Connection connection = ConnectToDB.getConnection(); PreparedStatement preparedStatement = connection.prepareStatement(
                "INSERT INTO Takes (Stud_ID,Sec_ID,Grade)"
                + "VALUES (?, ?, ?);")) {

            // Set values for the prepared statement
            preparedStatement.setInt(1, std_id);
            preparedStatement.setInt(2, section_id);
            preparedStatement.setString(3, grade);
            
            // Execute the insert operation
            preparedStatement.executeUpdate();

            // Success message
            JOptionPane.showMessageDialog(this, "Student Grade Added Successfully");
            
        } catch (SQLException e) {
            Logger.getLogger(SignUp.class.getName()).log(Level.SEVERE, "Database error: ", e);
            JOptionPane.showMessageDialog(this, "An error occurred while saving This data. Please try again.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

